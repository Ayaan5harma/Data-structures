//Brute force approach

int main()
{
    int n;
    cin>>n;
    vector<int> arr ={};


    for(int i=0;i<n;i++)
    {
        for(int j=0;j<=i;j++)
        {
            sort(arr.begin(),arr.end());
            
        }
    }
}